/*Abstract Classes and methods==>
Data abstraction is the process of hiding certain details and showing only
essential information to the user.
Abstraction can be achieved with either abstract classes or interfaces.

The abstract keyword is used for classes and methods:
Abstract class: this type of class object cannot be created (to access it, it must
be inherited from another class).

Abstract method: An abstract method in Java is declared through the keyword 
"abstract". While the declaration of the abstract method, the abstract keyword
has to be placed before the name of the method. There is no body in an abstract
method. 
*/